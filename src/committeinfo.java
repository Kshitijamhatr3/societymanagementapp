    public void displayInJtable(){
      ArrayList<committe> list =getUList();
      DefaultTableModel m=(DefaultTableModel)jTable1.getModel();
      Object[] row=new Object[4];
      for(int i=0;i<list.size();i++){
          row[0]=list.get(i).getId();
          row[1]=list.get(i).getMemberrole();
          row[2]=list.get(i).getWorkassign();
          row[3]=list.get(i).getWorkdn();
          m.addRow(row);
      }
    }
    public void sqlQuery(String query,String msg)
    {
      Connection con=getConnection();
      Statement s;
      try{
         s=con.createStatement();
         if((s.executeUpdate(query))==1){
             
             DefaultTableModel m=(DefaultTableModel)jTable1.getModel();
             m.setRowCount(0);
             displayInJtable();
             
             JOptionPane.showMessageDialog(null,"Data "+msg+"Successfull");
         }
         else{
            JOptionPane.showMessageDialog(null,"Data  Not "+msg); 
         }
      }
      catch(Exception e){
         e.printStackTrace(); 
      }
    }

    

