    public ArrayList<committe> getUList(){
     ArrayList<committe> usersList=new ArrayList<committe>();
     Connection con=getConnection();
     String query="SELECT * FROM `committeinfo`";
     Statement st;
     ResultSet rs;
     try{
         st=con.createStatement();
         rs=st.executeQuery(query);
         committe mem;
         while(rs.next()){
             mem=new committe(rs.getInt("id"),rs.getString("memrole"),rs.getString("workass"),rs.getString("workdone"));
             usersList.add(mem);
         }
    }
     catch(Exception e){
       e.printStackTrace();
     }
     return usersList;
    }
