    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int jt=jTable1.getSelectedRow();
        TableModel m=jTable1.getModel();
        jtf_rno.setText(m.getValueAt(jt,0).toString());
        String mrole=m.getValueAt(jt,1).toString();
        switch(mrole){
            case "Cashier":
            jcb.setSelectedIndex(0);
            break;
            case "Chairman":
            jcb.setSelectedIndex(1);
            break;
            case "Commitee Staff":
            jcb.setSelectedIndex(2);
            break;

        }
        jtf_wa.setText(m.getValueAt(jt,2).toString());
        String wdn=m.getValueAt(jt,3).toString();
        if(wdn.equals("yes")){
            jRadioButton1.setSelected(true);
        }
        else{
            jRadioButton2.setSelected(true);
        }

    }//GEN-LAST:event_jTable1MouseClicked

